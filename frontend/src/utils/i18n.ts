const dictionaries: Record<Language, TranslationDictionary> = { en, vi };

export type TranslationKey = keyof typeof en;

export const translate = (language: Language, key: string, params?: Record<string, string | number>) => {
  const template = dictionaries[language][key] ?? en[key] ?? key;
  if (!params) return template;
  return Object.entries(params).reduce((text, [name, value]) => text.replaceAll(`{${name}}`, String(value)), template);
};

export const getDictionary = (language: Language) => dictionaries[language];
